
# 2.7. Write a program that read radius of a circle and display the area

# delaring PI constant
# Python constant delaration is like variable declaration
# we just need to name the constant variable in all upper case
PI  = 3.1416

# taking radius as float
radius = float(input("enter a float variable: "))

# area equation
area = PI * radius * radius

print("Area is: ", area)

